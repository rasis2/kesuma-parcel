// Language stored in localStorage
function getLang() {
  return localStorage.getItem('kp_lang') || 'ms'
}

function setLang(lang) {
  localStorage.setItem('kp_lang', lang)
  location.reload()
}

function t(key) {
  const lang = getLang()
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['ms'][key] || key
}

// Render language switcher
function renderLangSwitcher(containerId) {
  const langs = [
    { code: 'ms', label: 'BM' },
    { code: 'en', label: 'EN' },
    { code: 'zh', label: '中文' },
    { code: 'ta', label: 'தமிழ்' },
  ]
  const current = getLang()
  const el = document.getElementById(containerId)
  if (!el) return
  el.innerHTML = langs.map(l => `
    <button onclick="setLang('${l.code}')"
      style="padding:4px 10px;border-radius:6px;border:1px solid ${l.code === current ? 'var(--gold)' : 'var(--border)'};
      background:${l.code === current ? 'var(--gold-dim)' : 'transparent'};
      color:${l.code === current ? 'var(--gold)' : 'var(--text-muted)'};
      font-size:11px;font-weight:500;cursor:pointer;font-family:'DM Sans',sans-serif;
      transition:all 0.2s">${l.label}</button>
  `).join('')
}

